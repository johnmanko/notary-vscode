/**
 * Escapes HTML special characters to prevent XSS attacks.
 * @param s - The value to escape
 * @returns HTML-escaped string
 */
export function escapeHtml(s: unknown): string {
	return String(s)
		.replaceAll('&', '&amp;')
		.replaceAll('<', '&lt;')
		.replaceAll('>', '&gt;')
		.replaceAll('"', '&quot;');
}
